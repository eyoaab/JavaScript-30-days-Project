var month=prompt("enter month");
if(month=="march" || month=="april" || month=="may"){
    console.log("spring");
}
else if(month=="june" || month=="july" || month=="august"){
    console.log("summer");
}
else if(month=="september" || month=="october" || month=="november"){
    console.log("autumn");
    
}
else{
    console.log("winter");
}
